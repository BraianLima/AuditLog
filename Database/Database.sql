CREATE DATABASE Test

USE Test

CREATE TABLE Users
(
	Id INT PRIMARY KEY NOT NULL IDENTITY(1,1),
	Name VARCHAR(50) NOT NULL,
	Password VARCHAR(2000) NOT NULL,
	CreatedDate DATETIME2 NOT NULL,
	LastLoginDate DATETIME2
)

CREATE TABLE Orders
(
	Id INT PRIMARY KEY NOT NULL IDENTITY(1,1),
	ProductName VARCHAR(20) NOT NULL,
	UserId INT CONSTRAINT FK_order_user0_user FOREIGN KEY REFERENCES Users(Id) NOT NULL
)



CREATE TABLE Audits
(
	Id INT PRIMARY KEY NOT NULL IDENTITY(1,1),
	Action VARCHAR(10) NOT NULL,
	TableName VARCHAR(30) NOT NULL,
	OldObject VARCHAR(MAX),
	NewObject VARCHAR(MAX),
	CreatedDate DATETIME2 NOT NULL,
	UserId INT CONSTRAINT FK_audits_users0_users FOREIGN KEY REFERENCES Users(Id) NOT NULL
)

